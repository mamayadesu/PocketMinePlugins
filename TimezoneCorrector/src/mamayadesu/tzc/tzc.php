public function onCommand(CommandSender $sender, Command $command, $label, array $params)
    {
        switch($command->getName())
        {
            case "timezone":
                $set = array_shift($params);
                $timezone = implode("", $params);
                if(! empty($set))
                {
                    if($set == "set")
	                {
                            if(! empty($timezone))
	                        {
                                if($this->getTimezone($timezone))
                                {
                                    $this->getConfig()->set("timezone", $timezone);
                                    $this->getConfig()->save();
                                    date_default_timezone_set($this->getConfig()->get("timezone"));
                                    $sender->sendMessage("New timezone (".$timezone.") is set!");
                                    return true;
                                }
                                else
                                {
                                    $sender->sendMessage("Invalid timezone!");
                                    return true;
                                }
                            }
                        else return false;
                    }
                    else
                    {
                        $sender-sendMessage("Unknow subcommand!");
                        return true;
                    }
                }
                else
                {
                    $sender->sendMessage(date("d/m/Y H:i:s", time())." - ".$this->getConfig()->get("timezone"));
                    return true;
                }
                break;
        }
    }
